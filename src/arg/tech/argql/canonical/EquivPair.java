package arg.tech.argql.canonical;


public class EquivPair {

	private INode i1;
	private INode i2;
	
	public EquivPair(INode i1, INode i2) {
		this.i1 = i1;
		this.i2 = i2;
	}

	public INode getI1() {
		return i1;
	}

	public void setI1(INode i1) {
		this.i1 = i1;
	}

	public INode getI2() {
		return i2;
	}

	public void setI2(INode i2) {
		this.i2 = i2;
	}



}
